/** Normalize text for search: trim, lowercase, collapse spaces. */
function normalize(s: string): string {
  return s.trim().toLowerCase().replace(/\s+/g, ' ')
}

/**
 * Search the knowledge base by a free-text query.
 * Empty/whitespace-only query returns all entries.
 * Matches against q, a, category, and keywords (case-insensitive, Thai-friendly).
 */
export function searchKb(query: string, kb: KbEntry[] = KB): KbEntry[] {
  const q = normalize(query)
  if (!q) return kb
  return kb.filter(entry => {
    if (normalize(entry.q).includes(q)) return true
    if (normalize(entry.a).includes(q)) return true
    if (normalize(entry.category).includes(q)) return true
    if (entry.keywords) {
      for (const kw of entry.keywords) {
        if (normalize(kw).includes(q) || q.includes(normalize(kw))) return true
      }
    }
    return false
  })
}

/**
 * Return distinct category names in first-seen order from the given array.
 */
export function kbCategories(kb: KbEntry[]): string[] {
  const seen = new Set<string>()
  const out: string[] = []
  for (const entry of kb) {
    if (!seen.has(entry.category)) {
      seen.add(entry.category)
      out.push(entry.category)
    }
  }
  return out
}
